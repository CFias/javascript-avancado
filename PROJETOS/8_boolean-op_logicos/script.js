// Com o && - And, o False prevalece
console.log(24 > 25 && 'Fias' == 'Fias');

// Com o || - Or, o True prevalece
console.log(25 > 24 || 3 == 1);

console.log('Cleidson' == 'Silva' || false)
console.log(3 == 3 && 'Fias' == 'Fias')

// Com o ! - Not, é foraçado o oposto ou o segundo valor prealece.
console.log(!(true && true));
console.log(!(false && true));
console.log(!('Fias' != 'Ramos'));

console.log(true && true)
console.log(false || false)